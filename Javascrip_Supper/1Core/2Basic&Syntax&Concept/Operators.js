// +	        Addition
// -	        Subtraction
// '*'	        Multiplication
// '**'	        Exponentiation (ES2016)
// /	        Division
// %	        Modulus (Division Remainder)
// ++	        Increment
// --	        Decrement

// *2. Toán tử gán (Assignment Operators)
// Toán tử gán được sử dụng để gán giá trị cho một biến, và kết hợp thực hiện các phép toán số học.

// Toán tử	    Tên	                            Mô tả	                                                                                Ví dụ	        Kết quả
// =	        Gán	                            Gán giá trị bên phải cho biến bên trái.	                                                x = 5	        x = 5
// +=	        Cộng rồi gán	                Cộng giá trị hiện tại của biến với giá trị bên phải, sau đó gán lại.	                x += 5	        x = x + 5
// -=	        Trừ rồi gán	                    Trừ giá trị bên phải từ giá trị hiện tại của biến, sau đó gán lại.	                    x -= 5	        x = x - 5
// '*='	        Nhân rồi gán	                Nhân giá trị hiện tại của biến với giá trị bên phải, sau đó gán lại.	                x *= 5	        x = x * 5
// /=	        Chia rồi gán	                Chia giá trị hiện tại của biến cho giá trị bên phải, sau đó gán lại.	                x /= 5	        x = x / 5
// %=	        Lấy dư rồi gán	                Lấy phần dư của biến chia cho giá trị bên phải, sau đó gán lại.	                        x %= 3	        x = x % 3
// '**='	        Lũy thừa rồi gán	        Lũy thừa giá trị hiện tại của biến với giá trị bên phải, sau đó gán lại (ES6+).	        x **= 3	        x = x ** 3
// <<=	        Dịch trái rồi gán	            Dịch bit của biến sang trái một số vị trí, sau đó gán lại.	                            x <<= 2	        x = x << 2
// >>=	        Dịch phải rồi gán	            Dịch bit của biến sang phải một số vị trí, sau đó gán lại.	                            x >>= 2	        x = x >> 2
// &=	        Phép AND trên bit rồi gán	    Thực hiện AND bit giữa giá trị hiện tại và giá trị bên phải, sau đó gán lại.	        x &= 3	        x = x & 3
// `	        =`	                            Phép OR trên bit rồi gán	                                                           Thực hiện OR bit giữa giá trị hiện tại và giá trị bên phải, sau đó gán lại.	                            `x
// ^=	        Phép XOR trên bit rồi gán	    Thực hiện XOR bit giữa giá trị hiện tại và giá trị bên phải, sau đó gán lại.	        x ^= 3	        x = x ^ 3
